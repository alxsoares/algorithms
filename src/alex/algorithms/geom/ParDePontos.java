package alex.algorithms.geom;

public class ParDePontos {

	private Ponto p2;
	private Ponto p1;

	public ParDePontos(Ponto p1, Ponto p2) {
		this.p1 = p1;
		this.p2 = p2;
	}

	public Ponto getP2() {
		return p2;
	}

	public void setP2(Ponto p2) {
		this.p2 = p2;
	}

	public Ponto getP1() {
		return p1;
	}

	public void setP1(Ponto p1) {
		this.p1 = p1;
	}
	

}
